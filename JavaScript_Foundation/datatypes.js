// These are primitive datatypes in javascript.

// 1. Number
// Number can store float/decimal value
let a=54;

// typeof: This is used to check the datatype 
console.log(typeof(a));

let deci=45.26;

// typeof: This is used to check the datatype 
console.log(typeof(deci));

// 2. Character
let b='Z';

// typeof: This is used to check the datatype 
console.log(typeof(b));

//  3. String
let c="Bushbunian";

// typeof: This is used to check the datatype 
console.log(typeof(c));

// 4. Boolean
let d=true;

// typeof: This is used to check the datatype 
console.log(typeof(d));

// 5. Null
let name=null;

// typeof: This is used to check the datatype 
console.log(typeof(name));

// 6. Undefined
let age;

// typeof: This is used to check the datatype 
console.log(typeof(age));

// 7. BigInt
let salary=7903601157;

// typeof: This is used to check the datatype 
console.log(typeof(salary));

// 8. Symbol
let value=Symbol("123");

// typeof: This is used to check the datatype 
console.log(typeof(value));




// These are non-primitive datatypes in javascript.
// 'const' keyword is used in case of 'object' and 'array'. 

// Object
// Syntax: const <object_name> = {<key_name1>:<value1>, <key_name2>:<value2>};

const user={place:"Bhubaneswar", name:"Bushbunian", age:26};

// typeof: This is used to check the datatype 
console.log(typeof(user));


// Array
// Syntax: const <array_name> = [<value1>, <value2>];
// Datatype of an array is also an object

const fruits=["Apple", "Grapes", "Mango"];

// typeof: This is used to check the datatype 
console.log(typeof(fruits));


