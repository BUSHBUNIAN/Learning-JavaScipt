// Operators are some symbols which are employed on variables to perform some computation.

// Types of operators:-

// 1. Arithmatic operator: Performs arithmatic operations
// if one of the operand is a string, then artithmatic operator converts the string into number and perform computation.

// Addition
let a=10, b=2, sum=a+b;
console.log("sum=",sum);

// Subtraction
diff=b-a;
console.log("diff=",diff);

// Multiplication
mul=b*a;
console.log("mul=",mul);

// Division
div=b/a;
console.log("div=",div);

// Exponentiation
exp=a**b;
console.log("exp=",exp);

// Modulous
mod=b%a;
console.log("mod=",mod);

// Post increment operator: Increment of the value will be occurred later. Here, a++ means a=a+1
console.log("Value of 'a'=",a++);
console.log("Value of 'a' (post increment)=",a);

// Pre increment operator: Increment of the value will be occurred first. Here, ++b means b=b+1
console.log("Value of 'b'=",++b);
console.log("Value of 'b' (pre increment)=",b);



// Post decrement operator: Decrement of the value will be occurred later. Here, a-- means a=a-1
console.log("Value of 'a'=",a--);
console.log("Value of 'a' (post decrement)=",a);

// Pre decrement operator: Decrement of the value will be occurred first. Here, --b means b=b-1
console.log("Value of 'b'=",--b);
console.log("Value of 'b' (pre decrement)=",b);
